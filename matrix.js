var matrix = [
  [
    "=====---",
    "=----=--",
    "=-----=-",
    "=------=",
    "=------=",
    "=-----=-",
    "=----=--",
    "=====---"
  ],
  [
    "---==---",
    "--=--=--",
    "--=--=--",
    "-=----=-",
    "=-----=-",
    "========",
    "=------=",
    "=------="
  ],
  [
    "=------=",
    "==----==",
    "=-=--=-=",
    "=--==--=",
    "=------=",
    "=------=",
    "=------=",
    "=------="
  ],
]

var ret = "Paste pls\n"

for (var i = 0; i < matrix.length; i++) {
  for (var j = 0; j < 8; j++) {
    var number = 0;
    for(var k = 0; k < 8; k++) {
      if(matrix[i][j].charAt(k) == '='){
        number += Math.pow(2,7-k)
      }
    }
    ret += `maxOne(${i+1},${j+1},${number});                     //  ${matrix[i][j]}\n`
  }
  ret += '\n';
}

